<h2 class="name">About PVTC</h2>
<div class="text">Punjab vocational training council PVTC was established in October 1998 on the basis of public private partnership by the Government of the Punjab. PVTC is utilizing Zakat Funds. </div>
<h2 class="name">PVTC winning Themes</h2>
<div class="text">A unique model of its kind in the entire muslim world which utilize Zakat as a tool for skills and economics empowerment as conceived by Mian Muhammad Shahbaz Sharif the chief Minister  of Punjab during his first tenure in 1998.</div>
<h2 class="name">PVTC Winning Themes</h2>
<ul>
<li class="text">A unique model of its kind in the entire Muslim World, which utilizes Zakat as a tool for skills and economic empowerment as conceived by Mian Muhammad Shahbaz Sharif, the Chief Minister of Punjab during his first tenure in 1998.</li>
<li class="text">According to Education & Employment Alliance (EEA) and International Youth Foundation (IYF) PVTC has pioneered way to channel Muslim Charity (Zakat) funds to support an effective model of vocational training for the permanent rehabilitation of the poorest youth of our society.</li>
<li class="text">A flag bearer of Punjab Growth Strategy to train 2 million trainees by the year 2018, envisaged by Mian Muhammad Shahbaz Sharif, Chief Minister Punjab.</li>
<li class="text">In collaboration with GIZ, PSDF, UNICEF, British council, and other national and international development agencies is extensively contributing towards the poverty alleviation through skill training.</li>
<li class="text">Besides implementing its own curricula for 92 trades is also implementing 13 Competency Based and 8 City & Guilds Courses to cater for growing demands of industrial, agricultural, health & service sectors in its 313 Vocational Training Institutes (VTIs).</li>
<li class="text">The only organization utilizing vacant / under-utilized Govt buildings for its 98% institutes.</li>
<li class="text">Annual Training Capacity is around 180,000 trainees with 73.39% employability ratio, the highest in Pakistan.</li>
